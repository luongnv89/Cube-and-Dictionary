package csc5021.objects;

/**
 * @author luongnv89
 *
 */
public class MyPoint {
	int x;
	int y;
	int z;
	/**
	 * @param x
	 * @param y
	 * @param z
	 */
	public MyPoint(int x, int y, int z) {
		this.x = x;
		this.y = y;
		this.z = z;
	}
	/**
	 * @return the x
	 */
	public int getX() {
		return x;
	}
	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}
	/**
	 * @return the z
	 */
	public int getZ() {
		return z;
	};
	
}
